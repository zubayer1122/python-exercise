#Modulus
# Modulus ( % ) হল প্রোগ্রামিং এ একটা অপারেটর, যেটা দুইটা সংখ্যাকে ভাগ করলে ভাগশেষ 
# (remainder) কত থাকে সেটা বের করে।
g = 55
h = 3
print(g%h)
#Exponentiation
# Exponentiation ( ** ) মানে হলো কোনো সংখ্যাকে ঘাত (power) হিসেবে তোলা।
# Python-এ ** অপারেটর দিয়ে এই কাজ করা হয়।
i = 5
j = 2
print(i**j)
#Floor division
# Floor Division ( // ) মানে হলো কোনো সংখ্যাকে 
# ভাগ করলে ভাগফল নিচের পূর্ণসংখ্যায় নামিয়ে আনা।
k = 4
l = 1187
print(k//l)



